def part2(INPUT):
    grid: list[list[str]] = [list(s) for s in INPUT.split("\n")]

    def has_loop(grid: list[list[str]]) -> bool:
        pos = 0, 0
        for i, line in enumerate(grid):
            for j in range(len(line)):
                if line[j] == "^":
                    pos = i, j

        dir = 0
        visited = set([(pos, dir)])

        while True:
            next_pos = (pos[0] + STEP[dir][0], pos[1] + STEP[dir][1])
            if next_pos[0] < 0 or next_pos[1] < 0:
                return False
            try:
                next_symbol = grid[next_pos[0]][next_pos[1]]
            except IndexError:
                return False

            if next_symbol == "#":
                dir = (dir + 1) % 4
            else:
                pos = next_pos

            if (pos, dir) in visited:
                return True
            visited.add((pos, dir))

    total = 0
    for i in range(len(grid)):
        # print(f"{i} / {len(grid)}")
        for j in range(len(grid[0])):
            if grid[i][j] == ".":
                grid[i][j] = "#"
                total += has_loop(grid)
                grid[i][j] = "."

    print(total)


