def part1(INPUT: str) -> None:
    locks = []
    keys = []
    for thing in INPUT.split("\n\n"):
        if thing[0] == "#":
            # lock
            lock = []
            rows = thing.split("\n")
            for i in range(len(rows[0])):
                num = 0
                for row in rows[1:]:
                    if row[i] == "#":
                        num += 1
                lock.append(num)
            locks.append(lock)
        elif thing[-1] == "#":
            # key
            key = []
            rows = thing.split("\n")
            for i in range(len(rows[0])):
                num = 0
                for row in rows[:-1]:
                    if row[i] == "#":
                        num += 1
                key.append(num)
            keys.append(key)

    total = 0
    for key in keys:
        for lock in locks:
            total += all(h1 + h2 <= 5 for h1, h2 in zip(lock, key))

    print(total)


part1(INPUT)
